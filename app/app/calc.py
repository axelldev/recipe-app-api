"""
Calculate functions
"""


def add(x: int, y: int) -> int:
    """Calculates the addition between x and y.

    :param x: The frist number to add.
    :type x: int
    :param y: The second number to add.
    """
    return x + y


def sub(x: int, y: int) -> int:
    """Calculates the subtraction between 2 numbers.

    Args:
        x (int): The first number to add.
        y (int): The second number to add.

    Returns:
        int: The subtraction of the two given arguments x and y.
    """

    return x - y
